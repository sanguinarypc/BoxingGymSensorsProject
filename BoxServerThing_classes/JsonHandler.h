#ifndef JSONHANDLER_H
#define JSONHANDLER_H

#include <Arduino.h> // Βασική βιβλιοθήκη Arduino, πιθανόν για τύπους όπως String.
                     // Η ArduinoJson.h θα συμπεριληφθεί πιθανότατα στο .cpp αρχείο.

/**
 * @class JsonHandler
 * @brief Μια βοηθητική (utility) κλάση που περιέχει στατικές μεθόδους
 * για την επεξεργασία (parsing) δεδομένων σε μορφή JSON.
 *
 * Δεδομένου ότι όλες οι μέθοδοι είναι στατικές, δεν χρειάζεται η δημιουργία
 * αντικειμένου αυτής της κλάσης για τη χρήση των λειτουργιών της.
 */
class JsonHandler {
public:
  /**
   * @brief Επεξεργάζεται μια εισερχόμενη συμβολοσειρά (String) που αναμένεται να είναι σε μορφή JSON.
   *
   * Η μέθοδος αυτή είναι υπεύθυνη για την ανάλυση του JSON και την εξαγωγή
   * των σχετικών δεδομένων. Η ακριβής λογική της επεξεργασίας (π.χ. ποιες τιμές
   * εξάγονται και πώς χρησιμοποιούνται) θα βρίσκεται στην υλοποίησή της στο .cpp αρχείο.
   *
   * @param incoming Η συμβολοσειρά (String) που περιέχει τα δεδομένα JSON προς επεξεργασία.
   * Αναμένεται ότι αυτή η συμβολοσειρά είναι ένα έγκυρο JSON object.
   */
  static void parseIncoming(const String &incoming);
};

#endif // JSONHANDLER_H